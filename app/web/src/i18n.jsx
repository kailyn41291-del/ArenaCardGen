// 從 navigator.language 推斷:zh-TW / zh-Hant → zh-TW、zh-CN / zh-Hans → zh-CN、ja → ja、其他 → en
function detectLocale() {
  const nav = (typeof navigator !== 'undefined' && (navigator.language || navigator.userLanguage)) || 'zh-TW';
  const lower = nav.toLowerCase();
  if (lower.startsWith('zh')) {
    if (/cn|hans|sg/.test(lower)) return 'zh-CN';
    return 'zh-TW';
  }
  if (lower.startsWith('ja')) return 'ja';
  return 'en';
}

const LOCALE_STORAGE_KEY = 'arena-cardgen-locale';

export function getInitialLocale() {
  try {
    const saved = localStorage.getItem(LOCALE_STORAGE_KEY);
    if (saved && STRINGS[saved]) return saved;
  } catch {}
  return detectLocale();
}

export function persistLocale(locale) {
  try { localStorage.setItem(LOCALE_STORAGE_KEY, locale); } catch {}
}

export const LocaleContext = createContext('zh-TW');

// useT() 回傳 t(key, params?) — params 用 {n} / {current} 等做簡單字串插值
export function useT() {
  const locale = useContext(LocaleContext) || 'zh-TW';
  return useCallback((key, params) => {
    const raw = (STRINGS[locale] && STRINGS[locale][key]) ?? STRINGS['zh-TW'][key] ?? key;
    if (!params) return raw;
    return String(raw).replace(/\{(\w+)\}/g, (_, k) => (params[k] ?? '') + '');
  }, [locale]);
}

